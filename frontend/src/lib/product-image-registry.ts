export interface ProductImageAttribution {
  source: "bundled" | "generated" | "licensed";
  label?: string;
  url?: string;
}

export interface ProductImageRegistryEntry {
  slug: string;
  name: string;
  aliases: readonly string[];
  categoryId: string;
  subcategoryId: string;
  localAssetPath: string;
  attribution?: ProductImageAttribution;
}

export function normalizeProductImageKey(value: string): string {
  return value
    .normalize("NFKD")
    .toLowerCase()
    .replace(/&/g, " and ")
    // Keep meaningful qualifiers such as "Grade A", "HYV", and "Potash".
    // Only the punctuation is discarded so product and filename tokens agree.
    .replace(/[()]/g, " ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const bundled = (label: string): ProductImageAttribution => ({
  source: "bundled",
  label,
});

/**
 * All locally curated product assets are indexed from their filenames. This
 * keeps clearly named curated assets available to product cards without
 * requiring another per-file import. Ambiguous filenames are deliberately
 * excluded and continue through the explicit registry or taxonomy fallback
 * path.
 */
const localCuratedProductImages = import.meta.glob(
  "../assets/AgriConnect Images/{Bio,Commercial & Industry Crops,daily_needs,Dietary,Fresh_Produce,Inputs & Tools,Market,Modern Farming,Other Agricultural,Processed,Specialty}/**/*.{avif,jpg,jpeg,png,webp}",
  { eager: true, import: "default", query: "?url" },
) as Record<string, string>;

function getAssetProductName(assetPath: string): string {
  const filename = assetPath.split("/").at(-1) ?? "";
  return filename
    .replace(/\.[^.]+$/, "")
    .replace(/^\d+[_\s-]+/, "")
    .replace(/_/g, " ");
}

function getAssetFolder(assetPath: string): string {
  const pathParts = assetPath.split("/");
  return pathParts.at(-2) ?? "uncategorized";
}

